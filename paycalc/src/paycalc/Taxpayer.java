package paycalc;

public class Taxpayer {

    int grosspay;
    String name;

    public void setGrosspay(int grosspay) {
        this.grosspay = grosspay;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getGrosspay() {
        return grosspay;
    }
    public String getName() {
        return name;
    }
}
